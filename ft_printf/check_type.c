#include "ft_printf.h"

t_bool	is_valid_type(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (TRUE);
	return (FALSE);
}
